function [  patches_cell_array_final, ...
            patches2_cell_array_final, ...
            patches_dense_cell_array_final, ...
            patches_normal_cell_array_final] = subFields_v1( index,...
                                                    patches_cell_array, ...
                                                    patches2_cell_array, ...
                                                    patches_dense_cell_array, ...
                                                    patches_normal_cell_array)
                                                
% Take only a sub-field of the extracted features

    for i=1:numel(patches_cell_array) 

        patches_cell_array_final{i} = patches_cell_array{i}(:,index);
        patches2_cell_array_final{i} = patches2_cell_array{i}(:,index);
        patches_dense_cell_array_final{i} = patches_dense_cell_array{i}(:,index);
        patches_normal_cell_array_final{i} = patches_normal_cell_array{i}(:,index);

    end

end

