function [  patches_cell_array_final, ...
            patches2_cell_array_final, ...
            patches_dense_cell_array_final, ...
            patches_normal_cell_array_final] = subFields( num1, num2,...
                                                    patches_cell_array, ...
                                                    patches2_cell_array, ...
                                                    patches_dense_cell_array, ...
                                                    patches_normal_cell_array)
                                                
% Take only a sub-field of teh extracted features

    for i=1:numel(patches_cell_array) 

        patches_cell_array_final{i} = patches_cell_array{i}(:,num1:num2);
        patches2_cell_array_final{i} = patches2_cell_array{i}(:,num1:num2);
        patches_dense_cell_array_final{i} = patches_dense_cell_array{i}(:,num1:num2);
        patches_normal_cell_array_final{i} = patches_normal_cell_array{i}(:,num1:num2);

    end

end

